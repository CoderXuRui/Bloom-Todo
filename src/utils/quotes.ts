// 随机获取一条语录
export function getRandomQuote() {
  return quotes[Math.floor(Math.random() * quotes.length)];
}

// 基于日期的伪随机，同一天刷新会显示不同名言
export function getDailyQuote() {
  const today = new Date();
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate();
  // 用种子生成一个随机索引
  const index = (seed * 9301 + 49297) % 233280;
  const randomIndex = Math.floor((index / 233280) * quotes.length);
  return quotes[randomIndex];
}

// 获取当天的所有名言（打乱顺序），用于同一天不同刷新显示不同内容
export function getDailyQuoteShuffled() {
  const today = new Date();
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate();

  // 简单的 Fisher-Yates 洗牌，用日期做种子
  const shuffled = [...quotes];
  let currentSeed = seed;
  for (let i = shuffled.length - 1; i > 0; i--) {
    currentSeed = (currentSeed * 9301 + 49297) % 233280;
    const j = Math.floor((currentSeed / 233280) * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}